n1 = int(input('Digite um valor inteiro: '))
n2 = int(input('Digite outro valor inteiro: '))
s = n1+n2
m = n1*n2
d = n1/n2
di = n1//n2
sb = n1-n2
pt = n1**n2
print('A soma vale {} \no produto vale {}\na divisão {:.3f}\na divisão inteira {}\na subtração {}  \na potenciação {}'
      .format(s, m, d, di, sb, pt), end=' ')
print('Esse é os resultado da potenciação')
